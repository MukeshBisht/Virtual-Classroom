    private void setUserDetails(){
        User user = CurrentUser.getCurrentUser().getUser();
        username = user.getUsername();
        
        String[] userDetail = db.getUserDetails( user.getUsername() );
        
        nameField.setText (userDetail[1]);
        idField.setText (userDetail[2]);
        phoneField.setText (userDetail[3]);
        
        helloLabel.setText("Hello, " + username + " ("+ user.getType()+")");
        
        setAllSubject();
        setFacultySubject (username);
        
    }
    
    
    private void setAllSubject(){
        
        ArrayList <String> subject = db.getAllSubjects();
        allSubjectComboBox.removeAll();
        
        for (String s :subject){
            allSubjectComboBox.addItem(s);
        }
    }
    
    private void setFacultySubject(String username){
    
        ArrayList <String> subject = db.getSubjectByFacultyName (username);
        subjectComboBox.removeAll();
        
        for (String s :subject){
            subjectComboBox.addItem(s);
        }
        
    }
    
